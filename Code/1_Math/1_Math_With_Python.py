# Addition and Subtraction
print(10 + 15) # 25
print(10.0 + 15) # 25.0
print(108 - 8) # 100
print(108 - 8.0) # 100.0

# Multiplication
print(2 * 10) # 20
print(2.0 * 10) # 20.0
# Division
print(200 / 2) # 100.0
# Integer Division
print(5 // 2) # 2

# Modulus
print(5 % 2) # 1

# Exponentiation
print(2 ** 3) # 8

# Order of Operations PEMDAS
# Parenthese Exponents Multiplication Division Addition Subtraction
print(5 + 2 * (1 + 2) - 8 / 2) # 7.0